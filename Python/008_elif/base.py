x = input("enter a number ")
y = input("enter another number ")
z = input("put in an operation ")
if z == "+":
    print("Your number is" + (x + y))
if z == "-":
    print("Your number is" + (x - y))
if z == "*":
    print("Your number is" + (x * y))
if z == "/":
    print("Your number is " + (x / y))